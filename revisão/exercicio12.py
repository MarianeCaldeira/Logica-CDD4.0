"""Faça um algoritmo que receba o valor da base e da altura de um triângulo e calcule sua área.
Usando a fórmula A = (base x Altura ) / 2"""

base = float(input("Informe o valor da base do triângulo: "))
altura = float(input("Informe o valor da altura do triângulo: "))

area = (base * altura) / 2
print("A área do triângulo é:", area)
